    // methode permettant d'avoir l'id de l'admin selon l'id de l'utilisateur entree
    private void idadminSelonIduser(){
        
         try {
             // recupere l'id de l'utilisateur entrer dans le item et donne ses informations qui sont dans la bd
             String iduserString = comboBoxIduser.getSelectedItem().toString().split(" ")[0];
             int iduser = Integer.parseInt(iduserString);
             
             Utilisateur user = controlleur.routeVersGetUtilisateurIduser(iduser);
            
             
             // recupere l'id de l'admin conaissant l'id de l'utilisateur
             Admin admin = controlleur.routeVersGetAdminIdadmin(user.getIdAdmin());
             comboBoxIdadmin.setSelectedItem(admin.getIdadmin()+ " " + admin.getLogin());
             
         } catch (SQLException ex) {
             Logger.getLogger(JFrameSaveCompte.class.getName()).log(Level.SEVERE, null, ex);
         }
        
        
        
    }
    

